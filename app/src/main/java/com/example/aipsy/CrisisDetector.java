package com.example.aipsy;

import java.util.Arrays;
import java.util.List;

public class CrisisDetector {

    private static final List<String> CRISIS_ROOTS = Arrays.asList(
            "хочу умер", "хочу умр", "хочу уйти из жизни", "уйти из жизни",
            "убью себя", "убить себя", "хочу убить себ",
            "покончить с собой", "покончить с жизнью", "покончу с",
            "суицид", "самоубийств",
            "не хочу жить", "жить не хочу", "незачем жить", "нет смысла жить",
            "смысла жить нет", "больше не хочу жить", "лучше умер",
            "хочу исчезнуть навсегда", "меня скоро не будет", "скоро меня не будет",
            "ухожу из жизни", "прощайте навсегда", "больше не существу",
            "хочу всё закончить", "хочу всё закончit", "хочу закончить всё",
            "думаю о суициде", "думаю о смерти", "планирую уйти навсегда",
            "способ умереть", "как умереть", "как убить себя",
            "порежу себя", "порежусь", "порезать себя",
            "причиню себе вред", "навредить себе", "хочу навредить",
            "хочу навредит", "причинить себе",
            "выпрыгну", "прыгну с", "выброшусь из", "повешусь", "повеситься",
            "задушусь", "отравлюсь", "застрелюсь",
            "больше не буду жить", "не буду больше жить",
            "лучше бы меня не было", "лучше бы я не родил",
            "меня скоро не будет", "это мои последние"
    );

    private static final List<String> STRESS_ROOTS = Arrays.asList(
            "невыносимо", "больше не могу", "сил больше нет", "нет сил",
            "всё рухнуло", "хочу всё бросить", "хочу сбежать",
            "ненавижу себя", "я ничтожеств", "никому не нужен", "никому не нужна",
            "меня никто не любит", "никто не любит меня",
            "все против меня", "нет выхода", "тупик", "я сломан", "я сломлен",
            "я в отчаяни", "полное отчаяни", "не вижу смысла",
            "зачем я вообще", "зачем мне вообще"
    );

    public enum Level {
        NORMAL,
        HIGH_STRESS,
        CRISIS
    }

    public static Level analyze(String text) {
        if (text == null || text.isEmpty()) return Level.NORMAL;
        String lower = text.toLowerCase().trim();

        for (String root : CRISIS_ROOTS) {
            if (lower.contains(root)) return Level.CRISIS;
        }
        for (String root : STRESS_ROOTS) {
            if (lower.contains(root)) return Level.HIGH_STRESS;
        }
        return Level.NORMAL;
    }
}
